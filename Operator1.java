class  Operator1
{
	public static void main(String args[])
	{
		int n1=60,n2=30;
		char operator='/';
		if (operator=='+')
		{
			System.out.println(n1+n2);
		}
		if (operator=='-')
		{
			System.out.println(n1-n2);
		}
		if (operator=='*')
		{
			System.out.println(n1*n2);
		}
		if (operator=='/')
		{
			System.out.println(n1/n2);
		}
	}
}
