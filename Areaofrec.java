class Areaofrec
{
	public static void main(String args[])
	{
		int l=5,b=4;
		int aor=l*b;//20
		int por=2*(l+b);//18
		System.out.println("AOR  "+aor);
		System.out.println("POR  "+por);
		if(aor>por)
		{
			System.out.println("AOR IS GREATER THAN POR");
		}
		else
		{
			System.out.println("AOR IS LOWER THAN POR");
		}
	}
}